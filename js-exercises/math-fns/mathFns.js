// You can use the function from the `Math` module.
const sqrt = (number) => {
  return Math.sqrt(number)
}

const power = (number, power) => {
  return Math.pow(number, power)
}

const round = (number) => {
  return Math.round(number)
}

// Don't change the exported names. 
export {
  sqrt,
  power,
  round,
};
